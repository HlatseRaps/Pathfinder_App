    // Rendering logic
    const chapterListEl   = document.getElementById("chapterList");
    const chapterTitleEl  = document.getElementById("chapterTitle");
    const chapterKickerEl = document.getElementById("chapterKicker");
    const chapterContentEl= document.getElementById("chapterContent");
    const chapterCountEl  = document.getElementById("chapterCount");
    const progressBarEl   = document.getElementById("progressBar");
    const progressTextEl  = document.getElementById("progressText");

    const btnPrev  = document.getElementById("btnPrev");
    const btnNext  = document.getElementById("btnNext");
    const btnSave  = document.getElementById("btnSave");
    const btnReset = document.getElementById("btnReset");

    const STORAGE_KEY = "pf_comm_skills_progress";
    let current = parseInt(localStorage.getItem(STORAGE_KEY) || "0", 10);
    if (Number.isNaN(current) || current < 0 || current > chapters.length - 1) current = 0;

    let currentLang = document.getElementById("languageSelect").value === "Setswana" ? "tn" : "en";
    document.getElementById("languageSelect").addEventListener("change", function() {
      currentLang = this.value === "Setswana" ? "tn" : "en";
      renderAll();
    });

    // Build chapter list
    function renderChapterList(){
      chapterListEl.innerHTML = "";
      chapters.forEach((ch, idx) => {
        const li = document.createElement("li");
        const btn = document.createElement("button");
        btn.className = "chapter_btn" + (idx === current ? " active" : "");
        btn.innerHTML = `<i class="far fa-file-alt"></i> ${ch.title[currentLang] || ch.title["en"]}`;
        btn.addEventListener("click", () => {
          current = idx;
          renderAll();
        });
        li.appendChild(btn);
        chapterListEl.appendChild(li);
      });
    }

    function renderChapter(){
      const ch = chapters[current];
      chapterTitleEl.textContent  = ch.title[currentLang] || ch.title["en"];
      chapterKickerEl.textContent = ch.kicker[currentLang] || ch.kicker["en"];
      chapterContentEl.innerHTML  = "";

      ch.blocks.forEach(block => {
        const div = document.createElement("div");
        div.className = "panel";
        const h4 = document.createElement("h4");
        h4.textContent = block.h[currentLang] || block.h["en"];
        div.appendChild(h4);

        if (block.p){
          const p = document.createElement("p");
          p.textContent = block.p[currentLang] || block.p["en"];
          div.appendChild(p);
        }
        if (block.ul){
          const ul = document.createElement("ul");
          (block.ul[currentLang] || block.ul["en"] || block.ul).forEach(item=>{
            const li = document.createElement("li");
            li.textContent = item;
            ul.appendChild(li);
          });
          div.appendChild(ul);
        }
        chapterContentEl.appendChild(div);
      });

      // Key takeaways
      if (ch.takeaway){
        const t = document.createElement("div");
        t.className = "panel";
        const h4 = document.createElement("h4");
        h4.textContent = currentLang === "tn" ? "Dintlha tsa botlhokwa" : "Key Takeaways";
        t.appendChild(h4);
        const ul = document.createElement("ul");
        (ch.takeaway[currentLang] || ch.takeaway["en"]).forEach(x=>{
          const li = document.createElement("li");
          li.textContent = x;
          ul.appendChild(li);
        });
        t.appendChild(ul);

        // Embed YouTube video if external_link exists and is a YouTube link
        if (ch.external_link && ch.external_link.length > 0) {
          const link = ch.external_link[0].a;
          // Only embed for YouTube links
          if (link.includes("youtube.com") || link.includes("youtu.be")) {
            // Convert to embed URL
            let embedUrl = link;
            if (link.includes("youtu.be/")) {
              const videoId = link.split("youtu.be/")[1].split("?")[0];
              embedUrl = `https://www.youtube.com/embed/${videoId}`;
            } else if (link.includes("watch?v=")) {
              const videoId = link.split("watch?v=")[1].split("&")[0];
              embedUrl = `https://www.youtube.com/embed/${videoId}`;
            }
            // Add spacing before the video
            const spacer = document.createElement("div");
            spacer.style.height = "20px";
            t.appendChild(spacer);

            const iframe = document.createElement("iframe");
            iframe.width = "100%";
            iframe.height = "315";
            iframe.src = embedUrl;
            iframe.title = "YouTube video";
            iframe.frameBorder = "0";
            iframe.allow = "accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture";
            iframe.allowFullscreen = true;
            // Add styling for rounded corners and shadow
            iframe.style.borderRadius = "16px";
            iframe.style.boxShadow = "0 2px 16px rgba(0,0,0,0.08)";
            iframe.style.display = "block";
            iframe.style.margin = "0 auto";
            t.appendChild(iframe);
          }
        }

        chapterContentEl.appendChild(t);
      }

      chapterCountEl.textContent = `Chapter ${current+1} of ${chapters.length}`;
      // enable/disable nav
      btnPrev.disabled = current === 0;
      btnNext.disabled = current === chapters.length - 1;
    }

    function renderProgress(){
      const pct = Math.round(((current+1) / chapters.length) * 100);
      progressBarEl.style.width = pct + "%";
      progressTextEl.textContent = `${pct}% complete`;
    }

    function renderAll(){
      renderStaticUI();
      renderChapterList();
      renderChapter();
      renderProgress();
    }

    btnPrev.addEventListener("click", ()=>{
      if (current > 0){
         current--; renderAll();
     }
    });
    btnNext.addEventListener("click", ()=>{
      if (current < chapters.length - 1){
         current++; renderAll(); 
        }
    });
    btnSave.addEventListener("click", ()=>{
      localStorage.setItem(STORAGE_KEY, String(current));
      alert("Progress saved!");
    });
    btnReset.addEventListener("click", ()=>{
      if (confirm("Reset your course progress?")){
        current = 0;
        localStorage.removeItem(STORAGE_KEY);
        renderAll();
      }
    });


    renderAll();

